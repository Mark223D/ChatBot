import UIKit
import Foundation

extension UIColor
{
    static var cbWhite: UIColor
    {
        return UIColor.init(red: 219/255, green: 219/255, blue: 219/255, alpha: 0.9)
    }
    
    static var cbLightBlue: UIColor
    {
        return UIColor.init(red: 114/255, green: 145/255, blue: 160/255, alpha: 0.9)
    }
    
    static var cbBlue: UIColor
    {
        return UIColor.init(red: 33/255, green: 49/255, blue: 76/255, alpha: 0.9)
    }
    
    static var cbDarkBlue: UIColor
    {
        return UIColor.init(red: 6/255, green: 8/255, blue: 32/255, alpha: 0.9)
    }
}
